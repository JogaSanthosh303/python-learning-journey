""" 
pass
break
continue

simply we can say

pass -> ignore
break -> terminate
continue -> skip (current iteration)


 """

# pass
if True:
    pass             # output : 


# break
for i in "santhosh" :
    if i=='h':
        break
    print(i)         # output : sant


# continue
for i in "santhosh":
    if i=='h':
        continue
    print(i)           # output : santos